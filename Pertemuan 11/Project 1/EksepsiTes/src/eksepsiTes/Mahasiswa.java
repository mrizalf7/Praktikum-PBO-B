package eksepsiTes;

public class Mahasiswa {
	private String nim;
	private String nama;
	
	public void setName(String inputName) {
		nama = inputName;
	}
	public String getName() {
		return nama;
	}
	public void setNim(String inputNim) {
		nim = inputNim;
	}
	public String getNim() {
		return nim;
	}
}
